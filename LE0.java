// Java Program to print the multiplication table of the number N using for loop

class Varsha {
    public static void main(String[] args)
    {
        int N = 2;
        for (int i = 1; i <= 10; i++) {
            System.out.println(N + " * " + i + " = " + N * i);
        }
    }
}